/* O while  começa avaliando a condição.
 Se for condição for avaliada como true,
o código no bloco de código será executado.
Se for avaliado  como false, o código no bloco de código não será executado
 e o loop terminará. */

 //anatomia
 while (condicao) {
    //..
  }


//Exemplo: 

let i = 1;

//Enquanto uma variável for menor que 10,
// mostre-a no console e incremente-a em 1:

while (i < 10) {
  console.log(i);
  i++;
}

// Output:
// 1
// 2
// 3
// 4
// 5
// 6
// 7
// 8
// 9


