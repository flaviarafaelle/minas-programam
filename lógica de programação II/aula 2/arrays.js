/*
Os Arrays (listas) são usados para armazenar vários valores em uma única variável.

Propriedades e Métodos de um Array:
O poder dos Arrays de JavaScript são as propriedades e métodos que podemos aplicar

1. Um array de carros
const marcaCarros = ["Ford", "Hyundai", "Fiat", "BMW", "Etios"]; //Array

2. Acessando elementos de um array através da posição (começa do zero)
marcaCarros[3];

3. Quantidade de elementos de um array (length)
marcaCarros.length

4. Adicionar elementos ao final do array (push)
marcaCarros.push("Honda");

5. Como adicionar um elemento em uma posição específica?
marcaCarros[5] = ("Honda")

6. Como pegar a última posição do array e adicionar algo nela?
marcaCarros[marcaCarros.length] = ("Honda")

7. Removendo o elemento da última posição do array
marcaCarros.pop()
*/

